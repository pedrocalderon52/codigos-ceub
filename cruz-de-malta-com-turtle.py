for i in range(4):
    tl.forward(100)
    tl.left(202.5)
    tl.forward(40)
    tl.left(67.5)
    tl.forward(40)
    tl.left(90)
    tl.forward(40)
    tl.left(67.5)
    tl.forward(40)
    tl.left(202.5)

tl.mainloop()